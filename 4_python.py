# Python Casting.

# Example of Integrs.
x = int(9);         # x will be 9
y = int(7.9);       # y will be 7
z = int("3");       # z will be 3

# Example of Floats.
c = float(7);       # c will be 7.0
v = float(7.9);     # v will be 7.9
b = float("3");     # b will be 3.0
n = float("9.9");   # n will be 9.9

# Example of Strings.
a = str("str");     # a will be 'str'
s = str("1");       # s wiil be '1'
d = str(3);         # d will be '3'
f = str("1.7");     # f will be 'f'
g = str(7.9);       # g will be 'g'
print (" ");

print ("Example Out put of Integrs");
print (x + 11);
print (y + 13);
print (z + 17);
print (" ");

print ("Example Out put of Floats");
print (c + 3.0);
print (v + 2.1);
print (b + 7.0);
print (n + 0.1);
print (" ");

